#include "ft_ori.h"

int	ft_size_int(int nb)
{
	int c;

	c = 0;
	if (nb == 0)
		return (1);
	if (nb < 0)
	{
		c++;
		nb = -nb;
	}
	while (nb)
	{
		nb /= 10;
		c++;
	}
	return (c);
}
